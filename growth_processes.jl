# ╔═╡ 88dc5784-4868-4cd7-8648-d274c8cbd215
function diamond(n, origin=CartesianIndex(0, 0))
	len = n == 0 ? 1 : 4n
	return Iterators.map(0:len-1) do i
		origin + CartesianIndex(
			i < 2n ? n - i : i - 3n,
			i < n ? i : i < 3n ? 2n - i : i - 4n,
		)
	end
end

# ╔═╡ c1aaeb61-d0ff-4fed-bf37-81764c93740d
neighbors(origin) = diamond(1, origin)

# ╔═╡ ea328bb6-6bc4-40ca-b420-6c2df56f70a1
function accumulate_central_growth(W, n; max_backprop=typemax(Int))
	T = fill!(similar(W), typemax(Int))
	T[0, 0] = 0
	for i in 1:2n
		for j in i:-1:max(1, i-max_backprop)#1
			for I in diamond(j)
				I in CartesianIndices(W) || continue
				T[I] = W[I] + minimum(I -> get(T, I, typemax(Int)), neighbors(I)) + 1
			end
		end
	end
	return T
end

